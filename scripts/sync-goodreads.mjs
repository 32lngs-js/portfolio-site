#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────────
// Sync the bookshelf from a Goodreads shelf → regenerates ../books.js
//
// Goodreads killed its public API in Dec 2020. The surviving path is the
// per-shelf RSS feed, which the browser can't fetch (no CORS) — so this runs
// at build time in Node and writes a static books.js the page loads directly.
//
// The RSS gives you: title, author, isbn, rating, and COVER image URLs.
// It does NOT give spine images (nothing does). So each synced book renders as
// a CSS "faux spine" whose color is derived deterministically from the title.
// To get the real-spine look from the screenshot, hand-add a `spine:` image URL
// to any book in books.js afterwards — the sync preserves nothing, so keep a
// separate curated list if you mix. (See MODE notes in books.js.)
//
// Usage:
//   node scripts/sync-goodreads.mjs <goodreads_user_id> [shelf]
//   node scripts/sync-goodreads.mjs 12345678-your-name read
//
// Find your user id: open your Goodreads profile — the number in the URL
//   goodreads.com/user/show/<THIS>-name  is your user id.
// ─────────────────────────────────────────────────────────────────────────

import { writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const userId = process.argv[2]
const shelf = process.argv[3] || 'read'
if (!userId) {
  console.error('Usage: node scripts/sync-goodreads.mjs <goodreads_user_id> [shelf]')
  process.exit(1)
}

const feedUrl = `https://www.goodreads.com/review/list_rss/${userId}?shelf=${encodeURIComponent(shelf)}`
console.log(`Fetching ${feedUrl}`)

const res = await fetch(feedUrl, { headers: { 'User-Agent': 'bookshelf-sync' } })
if (!res.ok) {
  console.error(`Goodreads returned ${res.status}. Is the user id right and the profile public?`)
  process.exit(1)
}
const xml = await res.text()

// Minimal RSS parse. Goodreads wraps most fields in CDATA.
const items = xml.split('<item>').slice(1).map((chunk) => chunk.split('</item>')[0])
const pick = (chunk, tag) => {
  const m = chunk.match(new RegExp(`<${tag}>([\\s\\S]*?)</${tag}>`))
  if (!m) return ''
  return m[1].replace(/^<!\[CDATA\[/, '').replace(/\]\]>$/, '').trim()
}

// Deterministic spine color from the title so a shelf looks varied but stable.
const colorFor = (str) => {
  let h = 0
  for (let i = 0; i < str.length; i++) h = (h * 31 + str.charCodeAt(i)) >>> 0
  const hue = h % 360
  const sat = 55 + (h % 25)          // 55–80%
  const light = 30 + ((h >> 3) % 22) // 30–52% → mid/dark spines
  return { bg: `hsl(${hue} ${sat}% ${light}%)`, text: light < 55 ? '#ffffff' : '#111111' }
}

const books = items.map((chunk) => {
  const title = pick(chunk, 'title')
  const author = pick(chunk, 'author_name')
  const cover = pick(chunk, 'book_large_image_url') || pick(chunk, 'book_medium_image_url')
  const rating = Number(pick(chunk, 'average_rating')) || 0
  const { bg, text } = colorFor(title + author)
  // width nudged by title length so the shelf reads naturally
  const w = Math.max(24, Math.min(46, 22 + Math.round(title.length * 0.7)))
  return { title, author, cover, rating, spineColor: bg, text, w }
})

if (!books.length) {
  console.error('No books found. Check the shelf name and that the profile is public.')
  process.exit(1)
}

const header = `// AUTO-GENERATED by scripts/sync-goodreads.mjs — shelf "${shelf}", ${books.length} books.
// Edit books.js by hand to add real \`spine:\` images or \`faceOut\` covers; re-running
// this script overwrites the file. See scripts/sync-goodreads.mjs for the data model.
`
const body = books
  .map((b) => '  ' + JSON.stringify(b))
  .join(',\n')

const out = `${header}\nwindow.BOOKS = [\n${body},\n]\n`

const outPath = join(dirname(fileURLToPath(import.meta.url)), '..', 'books.js')
await writeFile(outPath, out)
console.log(`Wrote ${books.length} books → ${outPath}`)
